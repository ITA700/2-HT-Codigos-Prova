c = float(input("Digite a temperatura da sua cidade *Obrigatoriamente em C° "))
conversao_fahrenheit = 1.8*c+32
print("A sua temperatura convertida de celsius para fahrenheit é: ", conversao_fahrenheit, " °F")
f = float(input("Digite a temperatura da sua cidade *Obrigatoriamente em F° "))
conversao_celsius = (f-32)/1.8
print("A sua temperatura convertida de celsius para fahrenheit é: ", conversao_celsius, " °C")
